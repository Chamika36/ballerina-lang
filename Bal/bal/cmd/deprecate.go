package cmd

import (
	"bal/pkg/utils"
	"fmt"
	"github.com/spf13/cobra"
	"github.com/spf13/viper"
	"log"
)

func deprecateCmd() *cobra.Command {
	cmd := &cobra.Command{
		Use:     "deprecate",
		Short:   "Deprecates a published package",
		Long:    "",
		Example: "",
		Run: func(cmd *cobra.Command, args []string) {
			fmt.Println("called")
			_ = utils.ExecuteBallerinaCommand(javaCmdPass, cmdLineArgsPass)
		},
	}

	viper.SetConfigName("deprecate")
	viper.AddConfigPath("/home/wso2/Final_implementation/ballerina-lang/Bal/executables/config") // Update this with your actual config path
	viper.SetConfigType("toml")

	if err := viper.ReadInConfig(); err != nil {
		log.Fatalf("Error reading config file: %s", err)
	}

	if long := viper.GetString("help.base.long"); long != "" {
		cmd.Long = long
	}
	if examples := viper.GetString("help.base.examples"); examples != "" {
		cmd.Example = examples
	}

	cmd.Flags().String("message", "", "Use the given <msg> as the deprecation message")
	cmd.Flags().Bool("undo", false, "Undo deprecation of a package")

	return cmd

}

func init() {
	deprecateCmd := deprecateCmd()
	RootCmd.AddCommand(deprecateCmd)

}
